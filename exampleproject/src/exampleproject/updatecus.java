            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/javap","root","");
            String sql="UPDATE customer SET name=?,address=?,phone=?,gender=?,card=? WHERE number=?";
