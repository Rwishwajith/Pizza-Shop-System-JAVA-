    private void txtsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsearchKeyReleased
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/javap","root","");
            String sql="SELECT * FROM customer where name=?";
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1,txtsearch.getText());
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                String setid=rs.getString("number");
                txtnumber.setText(setid);
                String setname=rs.getString("name");
                txtname.setText(setname);
                String setaddress=rs.getString("address");
                txtaddress.setText(setaddress);
                String setphone=rs.getString("phone");
                txtphone.setText(setphone);
                String sex=rs.getString("gender");
                if(sex.equals("Male")){
                    male.setSelected(true);
                }else{
                    female.setSelected(true);
                }
                String cards=rs.getString("card");
                switch(cards){
                    case "No Card":
                        cmd.setSelectedIndex(0);
                        break;
                     case "Gold":
                        cmd.setSelectedIndex(1);
                        break;
                        case "Silver":
                        cmd.setSelectedIndex(2);
                        break;
                }
                     
            }
            con.close(); 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txtsearchKeyReleased

